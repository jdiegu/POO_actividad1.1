
package calculardistanciav3;

public class Punto {
    private float x,y;

    public Punto(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public Punto() {
    }

    public void setY(float y) {
        this.y = y;
    }
    public float getY() {
        return y;
    }
    
    public void setx(float x){
        this.x=x;
    }
    public float getx(){
        return x;
    }
}
